#include "3-calc.h"
/**
 * op_add - adds
 * Description: adds two numbers
 * @a: 1st operand
 * @b: second operand
 * Return: result
 */
int op_add(int a, int b)
{
  return (a + b);
}

/**
 * op_sub - subtracts
 * Description: subtracts
 * @a: 1st operand
 * @b: second operand
 * Return: result
 */
int op_sub(int a, int b)
{
  return (a - b);
}
/**
 * op_mul - multiplies
 * Description: multiplies
 * @a: 1st operand
 * @b: second operand
 * Return: result
 */
int op_mul(int a, int b)
{
  return (a * b);
}
/**
 * op_div - divides
 * Description: divides
 * @a: 1st operand
 * @b: second operand
 * Return: result
 */
int op_div(int a, int b)
{
  return (a / b);
}
/**
 * op_mod - remainder
 * Description: remainder
 * @a: 1st operand
 * @b: second operand
 * Return: result
 */
int op_mod(int a, int b)
{
  return (a % b);
}
